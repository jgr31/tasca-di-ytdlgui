    private void loadFilesFromFolder() {
    File folder = new File(System.getProperty("user.home"), "Downloads");
    //File folder = new File("C:/Users/jordi.gelabert/Downloads");

    allFiles.clear();

    File[] files = folder.listFiles();
    if (files == null) {
        return;
    }

    for (File f : files) {
        if (f.isFile()) {
            String mime = "application/octet-stream";
            try {
                String detected = Files.probeContentType(f.toPath());

                if (detected != null) {
                    mime = detected;
                }
            } catch (IOException ex) {
                // si falla, deixem el mime per defecte
            }

            long size = f.length();

            MediaFile mf = new MediaFile(
                f.getName(),
                size,
                mime,
                LocalDateTime.now(),
                f.getAbsolutePath()
            );

            allFiles.add(mf);
        }
    }

    // ðŸ”¹ Actualitzar la JList
    lstMedia.setListData(
    allFiles.stream()
            .map(MediaFile::getName)
            .toArray(String[]::new)
);


    // ðŸ”¹ Assignar model a JTable
    tableModel = new MediaTableModel(allFiles);
    tblMedia.setModel(tableModel);

    lblCountElements.setText(allFiles.size() + " elementos");
}


