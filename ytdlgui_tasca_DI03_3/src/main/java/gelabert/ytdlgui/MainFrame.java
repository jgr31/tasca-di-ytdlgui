    // ðŸ”¹ Quan el login tingui Ã¨xit, guardarem el token aquÃ­
public void onLoginSuccess(String token) {
    // guardar el token a MainFrame
    this.authToken = token;

    // ðŸ”¹ donar el token al component i arrencar el polling
    pollingComponent.setToken(token);
    pollingComponent.setRunning(true);

    // activar menÃºs i anar al panell principal
    setLoggedIn(true);
    showCard("MAIN");
}



